package editor;

/**
 * Un observateur est un objet souhaitant être notifié à chaque fois qu'un objet
 * Observable auprès duquel il se sera "inscrit" est modifié
 *
 * @see Observable
 */
public interface Observer {
	/**
	 * Cette méthode est executée lorsque l'objet Observable fait appel à sa
	 * méthode notifierObservateurs().
	 *
	 * @param o
	 *            L'objet Observable se passant lui même en paramètre,
	 *            permettant à l'Observer de se mettre à jour
	 */
	public void update(Observable o);
}
